import { Variants } from "framer-motion";

export const useSkillAnimation = () => {
  const skillVariants: Variants = {
    hidden: { opacity: 0, x: -20 },
    visible: (i: number) => ({
      opacity: 1,
      x: 0,
      transition: {
        delay: i * 0.1,
        duration: 0.3,
      },
    }),
  };

  return { skillVariants };
};
